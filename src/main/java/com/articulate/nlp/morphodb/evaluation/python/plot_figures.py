#!/usr/bin/env python3
"""
plot_figures.py: Generate publication-quality PDF figures from CSV results.

Reads CSV files generated by Java EvaluationRunner and produces 5 publication-quality
PDF figures for the COLM 2026 paper on morphological annotation agreement.

Usage:
    python3 plot_figures.py --input results/tables/ --output results/figures/
"""

import argparse
import os
import sys
import warnings
from pathlib import Path

import matplotlib.pyplot as plt
import matplotlib.patches as mpatches
import numpy as np
import pandas as pd
import seaborn as sns
from scipy import stats

warnings.filterwarnings('ignore')


def setup_style():
    """Configure matplotlib for publication-quality figures."""
    try:
        plt.style.use('seaborn-v0_8-whitegrid')
    except OSError:
        plt.style.use('seaborn-whitegrid')

    # Colorblind-friendly palette
    sns.set_palette("husl")
    plt.rcParams.update({
        'font.size': 11,
        'axes.labelsize': 12,
        'axes.titlesize': 13,
        'xtick.labelsize': 10,
        'ytick.labelsize': 10,
        'legend.fontsize': 10,
        'figure.facecolor': 'white',
        'axes.facecolor': 'white',
    })


def figure_1_scaling_curve(input_dir, output_dir):
    """
    Figure 1: Scaling curve showing Cohen's κ vs model size with tier breakdown.

    - X-axis: model size in billions (log scale)
    - Y-axis: Cohen's κ (average across all properties)
    - Colored by tier with distinct markers
    - Frontier models placed at right with vertical dashed separator
    - Linear regression through non-frontier models
    - Horizontal dashed line at κ=0.70 (substantial agreement threshold)
    - Vertical dashed lines at tier boundaries
    """
    csv_path = Path(input_dir) / 'model_kappa.csv'
    if not csv_path.exists():
        print(f"Warning: {csv_path} not found. Skipping Figure 1.")
        return

    df = pd.read_csv(csv_path)

    # Compute average kappa across word classes
    df['avg_kappa'] = df[['noun_kappa', 'verb_kappa', 'adj_kappa', 'adv_kappa']].mean(axis=1)

    # Separate frontier and non-frontier models
    non_frontier = df[df['size_b'] != -1].copy()
    frontier = df[df['size_b'] == -1].copy()

    fig, ax = plt.subplots(figsize=(10, 6))

    # Define tiers and colors
    tier_colors = {'Consumer': '#1f77b4', 'HPC': '#ff7f0e', 'Frontier': '#d62728'}
    tier_markers = {'Consumer': 'o', 'HPC': 's', 'Frontier': '*'}

    # Plot non-frontier models by tier
    for tier in ['Consumer', 'HPC']:
        tier_data = non_frontier[non_frontier['tier'] == tier]
        ax.scatter(tier_data['size_b'], tier_data['avg_kappa'],
                  color=tier_colors[tier], marker=tier_markers[tier],
                  s=150, alpha=0.7, label=tier, edgecolors='black', linewidth=1.5)

    # Linear regression on non-frontier models (log scale)
    if len(non_frontier) > 1:
        log_sizes = np.log10(non_frontier['size_b'].values)
        kappas = non_frontier['avg_kappa'].values
        coeffs = np.polyfit(log_sizes, kappas, 1)
        poly = np.poly1d(coeffs)
        x_line = np.logspace(np.log10(non_frontier['size_b'].min()),
                            np.log10(non_frontier['size_b'].max()), 100)
        y_line = poly(np.log10(x_line))
        r_squared = np.corrcoef(log_sizes, kappas)[0, 1] ** 2
        ax.plot(x_line, y_line, 'k--', linewidth=2, alpha=0.6,
               label=f'Linear fit (R² = {r_squared:.3f})')

    # Plot frontier models at far right
    if len(frontier) > 0:
        frontier_x = 10 ** (np.log10(non_frontier['size_b'].max()) + 0.5)
        ax.scatter([frontier_x] * len(frontier), frontier['avg_kappa'],
                  color=tier_colors['Frontier'], marker=tier_markers['Frontier'],
                  s=200, alpha=0.7, label='Frontier', edgecolors='black', linewidth=1.5)

        # Vertical dashed separator before frontier
        ax.axvline(x=frontier_x / 1.3, color='gray', linestyle='--', linewidth=1, alpha=0.5)
        ax.text(frontier_x / 1.3, ax.get_ylim()[1] * 0.98, 'Frontier →',
               fontsize=10, ha='right', va='top', color='gray')

    # Horizontal dashed line at κ=0.70
    ax.axhline(y=0.70, color='gray', linestyle='--', linewidth=1.5, alpha=0.6,
              label='Substantial agreement (κ=0.70)')

    # Vertical dashed lines for tier boundaries (on non-frontier data)
    ax.axvline(x=14, color='gray', linestyle=':', linewidth=1, alpha=0.4)
    ax.axvline(x=120, color='gray', linestyle=':', linewidth=1, alpha=0.4)
    ax.text(7, ax.get_ylim()[0] + 0.02, 'Consumer', fontsize=9, ha='center', color='gray', alpha=0.6)
    ax.text(50, ax.get_ylim()[0] + 0.02, 'HPC', fontsize=9, ha='center', color='gray', alpha=0.6)
    ax.text(500, ax.get_ylim()[0] + 0.02, 'Frontier', fontsize=9, ha='center', color='gray', alpha=0.6)

    ax.set_xscale('log')
    ax.set_xlabel('Model Size (Billion Parameters)', fontsize=12)
    ax.set_ylabel("Cohen's κ (Average)", fontsize=12)
    ax.set_title('Agreement Scaling with Model Size', fontsize=13, fontweight='bold')
    ax.legend(loc='lower right', frameon=True, fancybox=True, shadow=True)
    ax.grid(True, alpha=0.3)
    ax.set_ylim([0, 1.0])

    plt.tight_layout()
    output_path = Path(output_dir) / 'scaling_curve.pdf'
    plt.savefig(output_path, dpi=300, bbox_inches='tight', format='pdf')
    plt.close()
    print(f"✓ Generated {output_path}")


def figure_2_pairwise_heatmap(input_dir, output_dir):
    """
    Figure 2: Heatmap of pairwise agreement between models.

    - Read pairwise_agreement.csv into symmetric matrix
    - Use seaborn.heatmap with annotations
    - Models sorted by parameter size on both axes
    """
    csv_path = Path(input_dir) / 'pairwise_agreement.csv'
    if not csv_path.exists():
        print(f"Warning: {csv_path} not found. Skipping Figure 2.")
        return

    df = pd.read_csv(csv_path, index_col=0)

    # Ensure matrix is square and symmetric
    df = df.loc[df.columns]

    fig, ax = plt.subplots(figsize=(12, 10))

    sns.heatmap(df, annot=True, fmt='.2f', cmap='RdYlGn', vmin=0.5, vmax=1.0,
               cbar_kws={'label': 'Agreement Score'},
               square=True, linewidths=0.5, linecolor='gray', ax=ax)

    ax.set_title('Pairwise Model Agreement Matrix', fontsize=13, fontweight='bold', pad=20)
    ax.set_xlabel('Model', fontsize=12)
    ax.set_ylabel('Model', fontsize=12)

    plt.tight_layout()
    output_path = Path(output_dir) / 'pairwise_heatmap.pdf'
    plt.savefig(output_path, dpi=300, bbox_inches='tight', format='pdf')
    plt.close()
    print(f"✓ Generated {output_path}")


def figure_3_per_property_curves(input_dir, output_dir):
    """
    Figure 3: Per word-class agreement curves vs model size.

    - X-axis: model size (log scale)
    - Y-axis: Cohen's κ for each word class
    - Four lines (noun, verb, adjective, adverb)
    - Different color and marker per word class
    """
    csv_path = Path(input_dir) / 'model_kappa.csv'
    if not csv_path.exists():
        print(f"Warning: {csv_path} not found. Skipping Figure 3.")
        return

    df = pd.read_csv(csv_path)

    # Filter out frontier models for cleaner non-frontier curves
    df_non_frontier = df[df['size_b'] != -1].copy().sort_values('size_b')
    df_frontier = df[df['size_b'] == -1].copy()

    fig, ax = plt.subplots(figsize=(10, 6))

    word_classes = ['noun_kappa', 'verb_kappa', 'adj_kappa', 'adv_kappa']
    colors = ['#1f77b4', '#ff7f0e', '#2ca02c', '#d62728']
    markers = ['o', 's', '^', 'D']
    labels = ['Noun', 'Verb', 'Adjective', 'Adverb']

    # Plot non-frontier curves
    for wc, color, marker, label in zip(word_classes, colors, markers, labels):
        ax.plot(df_non_frontier['size_b'], df_non_frontier[wc],
               color=color, marker=marker, linestyle='-', linewidth=2,
               markersize=8, alpha=0.7, label=label)

    # Plot frontier points
    if len(df_frontier) > 0:
        frontier_x = 10 ** (np.log10(df_non_frontier['size_b'].max()) + 0.5)
        for wc, color, marker, label in zip(word_classes, colors, markers, labels):
            ax.scatter([frontier_x] * len(df_frontier), df_frontier[wc],
                      color=color, marker=marker, s=150, edgecolors='black',
                      linewidth=1.5, alpha=0.7)

    ax.set_xscale('log')
    ax.set_xlabel('Model Size (Billion Parameters)', fontsize=12)
    ax.set_ylabel("Cohen's κ", fontsize=12)
    ax.set_title('Agreement by Word Class', fontsize=13, fontweight='bold')
    ax.legend(loc='lower right', frameon=True, fancybox=True, shadow=True)
    ax.grid(True, alpha=0.3)
    ax.set_ylim([0, 1.0])

    plt.tight_layout()
    output_path = Path(output_dir) / 'per_property_curves.pdf'
    plt.savefig(output_path, dpi=300, bbox_inches='tight', format='pdf')
    plt.close()
    print(f"✓ Generated {output_path}")


def figure_4_error_rate_bars(input_dir, output_dir):
    """
    Figure 4: Bar chart of error rates by model.

    - X-axis: models sorted by size
    - Y-axis: average error rate (%)
    - Bars colored by tier
    - Frontier models at the right
    """
    csv_path = Path(input_dir) / 'error_rates.csv'
    if not csv_path.exists():
        print(f"Warning: {csv_path} not found. Skipping Figure 4.")
        return

    df = pd.read_csv(csv_path)

    # Ensure avg_error_rate exists; if not, compute from other columns
    if 'avg_error_rate' not in df.columns:
        error_cols = [col for col in df.columns if col not in ['model', 'tier', 'size_b']]
        df['avg_error_rate'] = df[error_cols].mean(axis=1)

    # Separate frontier and non-frontier
    df_non_frontier = df[df['size_b'] != -1].copy().sort_values('size_b')
    df_frontier = df[df['size_b'] == -1].copy()

    # Combine for plotting
    df_plot = pd.concat([df_non_frontier, df_frontier], ignore_index=True)

    tier_colors = {'Consumer': '#1f77b4', 'HPC': '#ff7f0e', 'Frontier': '#d62728'}
    colors = [tier_colors.get(tier, '#808080') for tier in df_plot['tier']]

    fig, ax = plt.subplots(figsize=(12, 5))

    bars = ax.bar(range(len(df_plot)), df_plot['avg_error_rate'] * 100, color=colors, edgecolor='black', linewidth=1.5)

    ax.set_xlabel('Model', fontsize=12)
    ax.set_ylabel('Average Error Rate (%)', fontsize=12)
    ax.set_title('Error Rates by Model', fontsize=13, fontweight='bold')
    ax.set_xticks(range(len(df_plot)))
    ax.set_xticklabels(df_plot['model'], rotation=45, ha='right')

    # Add vertical separator before frontier
    if len(df_frontier) > 0:
        sep_x = len(df_non_frontier) - 0.5
        ax.axvline(x=sep_x, color='gray', linestyle='--', linewidth=2, alpha=0.5)

    # Legend
    legend_elements = [mpatches.Patch(facecolor=tier_colors[tier], edgecolor='black', label=tier)
                      for tier in ['Consumer', 'HPC', 'Frontier'] if tier in df_plot['tier'].values]
    ax.legend(handles=legend_elements, loc='upper right', frameon=True, fancybox=True, shadow=True)

    ax.grid(True, axis='y', alpha=0.3)
    ax.set_ylim([0, max(df_plot['avg_error_rate'] * 100) * 1.15])

    plt.tight_layout()
    output_path = Path(output_dir) / 'error_rate_bars.pdf'
    plt.savefig(output_path, dpi=300, bbox_inches='tight', format='pdf')
    plt.close()
    print(f"✓ Generated {output_path}")


def figure_5_architecture_comparison(input_dir, output_dir):
    """
    Figure 5: Grouped bar chart comparing architectures in same-size pairs.

    - X-axis: pair names (same-size model pairs)
    - Y-axis: average Cohen's κ
    - Two bars per group (model_a vs model_b)
    - Aggregate per-property rows to mean if needed
    """
    csv_path = Path(input_dir) / 'architecture_pairs.csv'
    if not csv_path.exists():
        print(f"Warning: {csv_path} not found. Skipping Figure 5.")
        return

    df = pd.read_csv(csv_path)

    # Aggregate by pair_name and model
    agg_data = []
    for pair in df['pair_name'].unique():
        pair_data = df[df['pair_name'] == pair]
        model_a = pair_data['model_a'].iloc[0]
        model_b = pair_data['model_b'].iloc[0]
        kappa_a = pair_data['kappa_a'].mean()
        kappa_b = pair_data['kappa_b'].mean()
        agg_data.append({
            'pair_name': pair,
            'model_a': model_a,
            'model_b': model_b,
            'kappa_a': kappa_a,
            'kappa_b': kappa_b
        })

    df_agg = pd.DataFrame(agg_data)

    fig, ax = plt.subplots(figsize=(8, 5))

    x = np.arange(len(df_agg))
    width = 0.35

    bars1 = ax.bar(x - width / 2, df_agg['kappa_a'], width, label='Model A',
                  color='#1f77b4', edgecolor='black', linewidth=1.5, alpha=0.8)
    bars2 = ax.bar(x + width / 2, df_agg['kappa_b'], width, label='Model B',
                  color='#ff7f0e', edgecolor='black', linewidth=1.5, alpha=0.8)

    ax.set_xlabel('Model Pair', fontsize=12)
    ax.set_ylabel("Cohen's κ (Average)", fontsize=12)
    ax.set_title('Architecture Comparison (Same-Size Pairs)', fontsize=13, fontweight='bold')
    ax.set_xticks(x)
    ax.set_xticklabels(df_agg['pair_name'], rotation=45, ha='right')
    ax.legend(frameon=True, fancybox=True, shadow=True)
    ax.grid(True, axis='y', alpha=0.3)
    ax.set_ylim([0, 1.0])

    plt.tight_layout()
    output_path = Path(output_dir) / 'architecture_comparison.pdf'
    plt.savefig(output_path, dpi=300, bbox_inches='tight', format='pdf')
    plt.close()
    print(f"✓ Generated {output_path}")


def main():
    """Main entry point with CLI argument parsing."""
    parser = argparse.ArgumentParser(
        description='Generate publication-quality PDF figures from CSV results.',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python3 plot_figures.py --input results/tables/ --output results/figures/
  python3 plot_figures.py -i data/csv -o data/pdf
        """
    )
    parser.add_argument('--input', '-i', type=str, required=True,
                       help='Directory containing input CSV files')
    parser.add_argument('--output', '-o', type=str, required=True,
                       help='Directory for output PDF figures')

    args = parser.parse_args()

    input_dir = Path(args.input)
    output_dir = Path(args.output)

    # Validate input directory
    if not input_dir.exists():
        print(f"Error: Input directory {input_dir} does not exist.", file=sys.stderr)
        sys.exit(1)

    # Create output directory
    output_dir.mkdir(parents=True, exist_ok=True)

    # Setup matplotlib style
    setup_style()

    print(f"Reading CSVs from: {input_dir}")
    print(f"Writing PDFs to: {output_dir}")
    print()

    # Generate all figures
    figure_1_scaling_curve(input_dir, output_dir)
    figure_2_pairwise_heatmap(input_dir, output_dir)
    figure_3_per_property_curves(input_dir, output_dir)
    figure_4_error_rate_bars(input_dir, output_dir)
    figure_5_architecture_comparison(input_dir, output_dir)

    print()
    print("All figures generated successfully!")


if __name__ == '__main__':
    main()
